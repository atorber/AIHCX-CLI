          <div class="drawer-field-value">{{ formatDate(selectedPool?.createdAt) }}</div>
        </div>
        <div class="drawer-field">
          <div class="drawer-field-label">更新时间</div>
          <div class="drawer-field-value">{{ formatDate(selectedPool?.updatedAt) }}</div>
        </div>
      </div>
